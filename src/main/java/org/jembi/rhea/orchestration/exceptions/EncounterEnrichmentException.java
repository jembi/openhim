package org.jembi.rhea.orchestration.exceptions;

public class EncounterEnrichmentException extends Exception {

	private static final long serialVersionUID = 662724502939127174L;

	public EncounterEnrichmentException() {
		super();
	}

	public EncounterEnrichmentException(String message, Throwable cause) {
		super(message, cause);
	}

	public EncounterEnrichmentException(String message) {
		super(message);
	}

	public EncounterEnrichmentException(Throwable cause) {
		super(cause);
	}

}
